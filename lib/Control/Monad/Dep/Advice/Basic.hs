module Control.Monad.Dep.Advice.Basic where

import Control.Monad.Dep
import Control.Monad.Dep.Advice
import Data.Proxy

-- | Makes the function discard its result and always return 'mempty'.
returnMempty :: Advice ca cem Monoid
returnMempty =
  makeExecutionAdvice
    ( \action -> do
        _ <- action
        pure mempty
    )
